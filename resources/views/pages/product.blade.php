<script>
    function RenderMausac(product_id,size){
        $.ajax({
            url: '/colortab/'+product_id+'/'+size,
            type:'GET',
        }).done(function(response){
            $("#variationcolor").empty();
            $("#variationcolor").html(response);
            $("#quanty-variation").val(1);
        });  
    }

    function RenderGia(product_id,color,size){
        $.ajax({
            url: '/pricetab/'+product_id+'/'+color+'/'+size,
            type:'GET',
        }).done(function(response){
            $("#variationprice").empty();
            $("#variationprice").html(response);
            $("#quanty-variation").val(1);
        });  
    }

    function AddCartProductPage(id){
        $.ajax({
            url:'/Add-Cart-Product-Page/'+id+'/'+$("#quanty-variation").val(),
            type:'GET',
        }).done(function(response){
            RenderCart(response);
            alertify.success('Đã thêm sản phẩm vào giỏ hàng');
            $("#quanty-variation").val(1);
        });  
    }

    function RemoveCart(rowId){
        $.ajax({
            url:'/Remove-Cart/'+rowId,
            type:'GET',
        }).done(function(response){
            RenderCart(response);
            alertify.error('Đã xóa sản phẩm khỏi giỏ hàng');
        });  
    }

    function RenderCart(response){

        $("#change-item-cart").empty();
        $("#change-item-cart").html(response);
        $("#total-quanty-show").text($("#tongsoluong").val());
        $("#total-price-show").text($("#tonggia").val()+' VNĐ');
        $("#total-price-show2").text($("#tonggia").val()+' VNĐ');
    }
</script>